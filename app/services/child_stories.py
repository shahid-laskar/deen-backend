def get_stories_by_category(age_group: str | None = None, category: str | None = None) -> list[dict]:
    res = STORY_LIBRARY
    if category:
        res = [s for s in res if s["category"] == category]
    if age_group:
        res = [s for s in res if age_group in s["age_groups"]]
    
    # Return everything except the full 'content' for the list view
    list_view = []
    for s in res:
        summary = s.copy()
        summary.pop("content", None)
        list_view.append(summary)
    return list_view

def get_story_by_key(key: str) -> dict | None:
    for s in STORY_LIBRARY:
        if s["key"] == key:
            return s
    return None
